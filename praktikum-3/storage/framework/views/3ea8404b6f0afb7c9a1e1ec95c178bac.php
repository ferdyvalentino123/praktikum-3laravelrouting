        musim reguler dan pascamusim), serta kemenangan pascamusim terbaik sepanjang masa. Dengan total tembakan
        gabungan Curry dan Thompson, mereka berdua dianggap sebagai salah satu pasangan backcourt terbaik sepanjang
        masa. Warriors telah mendapatkan tujuh gelar NBA, jumlah ketiga terbanyak dalam sejarah NBA. Menurut
