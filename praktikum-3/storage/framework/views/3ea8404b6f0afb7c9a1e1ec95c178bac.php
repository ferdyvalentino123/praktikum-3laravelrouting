    <p> Warriors kembali memasuki periode sulit pada tahun 1980-an, sebelum menjadi tim langganan babak playoff
        menjelang tahun 1990-an dengan para pemain bintang seperti Tim Hardaway, Mitch Richmond, dan Chris Mullin.
        Ketiganya dikenal dengan sebutan "Run TMC". Setelah gagal merebut gelar, Warriors memasuki fase konsolidasi pada
        tahun 2000-an. Nasib Warriors berubah pada tahun 2010-an, mengantar era kesuksesan baru yang dipimpin oleh
        Stephen Curry. Setelah merekrut para pemain All-Star seperti Klay Thompson dan Draymond Green, Warriors kembali
        merebut gelar pada tahun 2015, sebelum memenangkan dua gelar lainnya pada tahun 2017 dan 2018 bersama mantan MVP
        liga, Kevin Durant.</p>

    <p> Warriors dijuluki Dubs sebagai kependekan dari huruf "W". Mereka juga memegang beberapa rekor NBA;
